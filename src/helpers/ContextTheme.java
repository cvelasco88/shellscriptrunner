package helpers;

import javax.swing.*;
import java.awt.*;

public class ContextTheme {

    private Color colorText;
    private Color colorButton;
    private Color colorPanel;
    private Color colorList;

    /* Getteres & Setters */

    public Color getColorText() {
        return colorText;
    }

    public void setColorText(Color colorText) {
        this.colorText = colorText;
    }

    public Color getColorButton() {
        return colorButton;
    }

    public void setColorButton(Color colorButton) {
        this.colorButton = colorButton;
    }

    public Color getColorPanel() {
        return colorPanel;
    }

    public void setColorPanel(Color colorPanel) {
        this.colorPanel = colorPanel;
    }

    public Color getColorList() {
        return colorList;
    }

    public void setColorList(Color colorList) {
        this.colorList = colorList;
    }
}
